#!/bin/bash

# ------------------------------------------------------------------------------
#
# run_gradle.bat: create the Java documentation.
#
# ------------------------------------------------------------------------------

echo "================================================================================"
echo "Start $0"
echo "--------------------------------------------------------------------------------"
echo "ora_bench - Oracle benchmark - Gradle: create the Java documentation."
echo "--------------------------------------------------------------------------------"
date +"DATE TIME : %d.%m.%Y %H:%M:%S"
echo "================================================================================"

(
    cd lang/java || exit
    
    if ! { gradle javadoc --warning-mode all; }; then
        exit 255
    fi

    rm -rf ../priv/docs_java
    mkdir ../priv/docs_java
    cp -R build/docs/* ../priv/docs_java
)

echo ""
echo "--------------------------------------------------------------------------------"
date +"DATE TIME : %d.%m.%Y %H:%M:%S"
echo "--------------------------------------------------------------------------------"
echo "End   $0"
echo "================================================================================"
